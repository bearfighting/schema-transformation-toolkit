export function createTypeScriptNamingStrategy(): NamingStrategy {
  return createNamingStrategy({
    typeName: {
      style: "pascal",
      emptyFallback: "GeneratedType",
      invalidPrefix: "_",
      reservedWords: [...TYPESCRIPT_RESERVED_WORDS],
      reservedWordHandling: "suffix",
      reservedWordSuffix: "_",
    },
    fieldName: {
      style: "camel",
      fallback: "quoted",
      invalidPrefix: "_",
      reservedWords: [...TYPESCRIPT_RESERVED_WORDS],
      reservedWordHandling: "suffix",
      reservedWordSuffix: "_",
    },
  });
}
